// Helper: get blog by slug
export function getBlogBySlug(slug) {
  return blogPosts.find((post) => post.slug === slug) || null;
}

// Helper: get related posts (same tags, exclude current)
export function getRelatedPosts(currentSlug, limit = 2) {
  const current = getBlogBySlug(currentSlug);
  if (!current) return [];

  return blogPosts
    .filter((post) => post.slug !== currentSlug)
    .map((post) => ({
      ...post,
      relevance: post.tags.filter((tag) => current.tags.includes(tag)).length,
    }))
    .sort((a, b) => b.relevance - a.relevance)
    .slice(0, limit);
}

// Helper: get all unique tags
export function getAllTags() {
  const tags = new Set();
  blogPosts.forEach((post) => post.tags.forEach((tag) => tags.add(tag)));
  return ['All', ...Array.from(tags)];
}

// Helper: format date
export function formatBlogDate(dateStr) {
  return new Date(dateStr).toLocaleDateString('en-IN', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  });
}
